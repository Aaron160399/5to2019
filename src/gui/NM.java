    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int idMeeting = insertMeeting();
        if (idMeeting != 0) {
            System.out.println("Meeting inserted with ID: " + idMeeting);
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                int idFriend = Integer.parseInt(jTable1.getValueAt(i, 0).toString());
                Meeting_has_friend mhf = new Meeting_has_friend(idFriend, idMeeting);
                insertMeetingNM(mhf);
            }
            JOptionPane.showMessageDialog(this, "Meeting added correctly");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    int insertMeeting(){
        //Creo mi DAO
        MeetingDao meetingDao = new MeetingDao();
        //Obtengo los datos
        String place = jTextField1.getText();
        //Obtengo la fecha
        Date date = jDateChooser1.getDate();
        //Descompongo la fecha en sus componentes
        int year = date.getYear();
        int month = date.getMonth();
        int day = date.getDate();
        //Obtengo la hora
        int hour = (int)jSpinner1.getValue();
        //Obtengo los minutos
        int minutes = (int)jSpinner2.getValue();
        //Creo el Timestamp
        Timestamp date_of = new Timestamp(year, month, day, hour, minutes, 0, 0);
        //Creo el objeto
        Meeting meeting = new Meeting(place, date_of);
        //Inserto una reuniÃ³n
        int id = meetingDao.insert_meeting(meeting);
        return id;
    }
    
    void insertMeetingNM(Meeting_has_friend pojo){
        Meeting_has_frien_Dao meeting_has_friend_Dao = new Meeting_has_frien_Dao();
        meeting_has_friend_Dao.insert_mhf(pojo);
    }
    
    void selectFriend(){
        Friend friend = (Friend)jComboBox1.getSelectedItem();
        Object[] tableData = {friend.getIdFriend(), friend.getName(), 
            friend.getPhoneNumber()};
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
        defaultTableModel.addRow(tableData);
    }
    
